import javax.swing.*;



public class MDS_RadioButton extends JRadioButton{



    public MDS_RadioButton() {
        super();    
    }
    
    
    
    public MDS_RadioButton(Action a) {
        super(a);    
    }
    
    
    
    public MDS_RadioButton(Icon icon) {
        super(icon);    
    }
    
    
    
    public MDS_RadioButton(Icon icon, boolean selected) {
        super(icon, selected);    
    }
    
    
    
    public MDS_RadioButton(String text) {
        super(text);    
    }
    
    
    
    public MDS_RadioButton(String text, boolean selected) {
        super(text, selected);  
    }
    
    
    
    public MDS_RadioButton(String text, Icon icon) {
        super(text, icon);    
    }
    
    
    
    public MDS_RadioButton(String text, Icon icon, boolean selected) {
        super(text, icon, selected);   
    }
    
    
    
}